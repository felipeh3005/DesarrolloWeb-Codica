const getNumberExplanation = (number) => {
  switch (number) {
    case 666:
      return ('¡Cuidado! Número del diablo');
    case 42:
      return ('La respuesta a la pregunta definitiva sobre la vida, el universo y todo lo demás');
    case 7:
      return ('Número de la suerte');
    default:
      return ('Solo un número más...');
  }
};
